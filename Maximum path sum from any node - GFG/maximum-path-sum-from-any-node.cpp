class Solution {
  public:
    //Function to return maximum path sum from any node in a tree.
    int ans = INT_MIN;
    int util(Node *root){
        if(root==NULL){
            return 0;
        }
        
        int left = util(root->left);
        int right = util(root->right);
        
        if(left<0){
            left=0;
        }
        if(right<0){
            right=0;
        }
        
        ans = max(ans, root->data+left+right);
        return root->data + max(left,right);
    }
    int findMaxSum(Node* root)
    {
        // Your code goes here
        int res = util(root);
        res = max(res, ans);
        return res;
        
    }
};

